        Somos todos Pontos de Cultura

        O Programa Cultura Viva é uma política cultural voltada para o reconhecimento e apoio à  atividades e processos culturais já desenvolvidas, que estimula a participação social, a colaboração e a gestão compartilhada de políticas públicas no campo da cultura. Fomentar essas práticas é também aprofundar a democracia cultural e, por que não, a cultura democrática.

        A Rede Cultura Viva é a plataforma do Cadastro Nacional de Pontos e Pontões de Cultura, instituído pela Lei Cultura Viva. É a ferramenta que permite a autodeclaração por parte das entidades e coletivos culturais, e o reconhecimento por parte do Estado, estabelecendo uma relação direta entre os Pontos de Cultura, o Ministério da Cultura, e os entes federados parceiros. A rede social dos fazedores e fazedoras de cultura do Brasil. 

        Para quem já é Ponto ou Pontão de Cultura, o Cadastro é uma forma de manter os dados atualizados, acompanhar a execução de plano de trabalho, enviar informação sobre atividades desenvolvidas, além de se manter informado a respeito de encontros, editais, apoios e articulações. 

        Para quem quer ser Ponto ou Pontão de Cultura, é só chegar! Insira seus dados no Cadastro e, a partir de uma seleção simplificada e da adesão à Política Nacional de Cultura Viva, a entidade e coletivo cultural poderão ser reconhecidos como Ponto ou Pontão de Cultura, passando a integrar a rede da Política Nacional de Cultura Viva.

        Curtiu? Então seja mais um ponto nesta teia da diversidade cultural brasileira!


